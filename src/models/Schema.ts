export const counterSchema = pgTable('counter', {
  id: serial('id').primaryKey(),
  count: integer('count').default(0),
  updatedAt: timestamp('updated_at', { mode: 'date' })
    .defaultNow()
    .$onUpdate(() => new Date())
    .notNull(),
  createdAt: timestamp('created_at', { mode: 'date' }).defaultNow().notNull(),
});

export const bookMetadata = pgTable('book_metadata', {
  bookId: text('book_id').primaryKey().unique().notNull(),
  metadata: json('book_metadata').$type<Record<string, string[]>>().notNull(),
  updatedAt: timestamp('updated_at', { mode: 'date' })
    .defaultNow()
    .$onUpdate(() => new Date())
    .notNull(),
  createdAt: timestamp('created_at', { mode: 'date' }).defaultNow().notNull(),
});

export const history = pgTable('history', {
  userId: text('user_id').notNull(),
  bookId: text('book_id').notNull(),
  bookName: text('book_name').notNull(),
  updatedAt: timestamp('updated_at', { mode: 'date' })
    .defaultNow()
    .$onUpdate(() => new Date())
    .notNull(),
  createdAt: timestamp('created_at', { mode: 'date', withTimezone: true }).defaultNow().notNull(),
}, table => [
  primaryKey({ columns: [table.bookId, table.userId, table.createdAt] }),
]);
